function readRecordsFromDisk(): HistoryRecord[] {
  ensureDirectories();
  try {
    const raw = fs.readFileSync(DB_FILE, 'utf-8');
    const records: HistoryRecord[] = JSON.parse(raw || '[]');
    return Array.isArray(records) ? records : [];
  } catch (err) {
    console.error('Error reading sessions database file:', err);
    return [];
  }
}

