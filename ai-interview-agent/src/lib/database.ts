export function saveSession(session: InterviewSession): HistoryRecord {
  ensureDirectories();
  const records = readRecordsFromDisk();

  const record: HistoryRecord = {
    id: session.id,
    candidateName: session.candidateName,
    role: session.role,
    experience: session.experience,
    overallScore: session.finalReport?.overallScore || 0,
    maxScore: session.finalReport?.maxScore || 50,
    status: session.finalReport?.status || 'In Progress',
    recommendation: session.finalReport?.recommendation || 'Pending',
    createdAt: session.createdAt || new Date().toISOString(),
    questionsCount: session.questions.length,
    data: session
  };

  const existingIdx = records.findIndex((r) => r.id === session.id);
  if (existingIdx >= 0) {
    records[existingIdx] = record;
  } else {
    records.unshift(record);
  }

  // Write to SQLite JSON DB
  fs.writeFileSync(DB_FILE, JSON.stringify(records, null, 2), 'utf-8');

  // Also write the latest session transcript to transcript.json as requested in outputs/transcript.json deliverable
  try {
    const transcriptOutput = {
      candidate_info: {
        id: session.id,
        name: session.candidateName,
        role: session.role,
        experience: session.experience,
        date: session.createdAt
      },
      questions_and_evaluations: session.evaluations,
      final_report: session.finalReport || null
    };
    fs.writeFileSync(TRANSCRIPT_FILE, JSON.stringify(transcriptOutput, null, 2), 'utf-8');
  } catch (err) {
    console.error('Error writing transcript.json output:', err);
  }

  return record;
}

export function getSessionById(id: string): HistoryRecord | undefined {
  const records = getAllSessions();
  return records.find((r) => r.id === id);
}

export function deleteSessionById(id: string): boolean {
  ensureDirectories();
  const records = getAllSessions();
  const filtered = records.filter((r) => r.id !== id);
  if (filtered.length !== records.length) {
    fs.writeFileSync(DB_FILE, JSON.stringify(filtered, null, 2), 'utf-8');
    return true;
  }
  return false;
}
